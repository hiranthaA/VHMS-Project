    public void grantAccess(){
        try{
            Statement stmnt = dbcon.createStatement();
            ResultSet rs = stmnt.executeQuery("select * from access_info where userID='"+accessedUser+"'");
            
            while(rs.next()){
                if(rs.getBoolean("accessPh")){
                    this.btnPharmacy.setEnabled(true);
                }
                else{
                    this.btnPharmacy.setEnabled(false);
                }
                if(rs.getBoolean("accessHos")){
                    this.btnHospital.setEnabled(true);
                }
                else{
                    this.btnHospital.setEnabled(false);
                }
                if(rs.getBoolean("accessMVS")){
                    this.btnMobileService.setEnabled(true);
                }
                else{
                    this.btnMobileService.setEnabled(false);
                }
                if(rs.getBoolean("accessPS")){
                    this.btnPetshop.setEnabled(true);
                }
                else{
                    this.btnPetshop.setEnabled(false);
                }
                if(rs.getBoolean("accessDC")){
                    this.btnDaycare.setEnabled(true);
                }
                else{
                    this.btnDaycare.setEnabled(false);
                }
                if(rs.getBoolean("accessHC")){
                    this.btnHealthcare.setEnabled(true);
                }
                else{
                    this.btnHealthcare.setEnabled(false);
                }
                if(rs.getBoolean("accessEmp")){
                    this.btnEmployee.setEnabled(true);
                }
                else{
                    this.btnEmployee.setEnabled(false);
                }
                if(rs.getBoolean("accessFin")){
                    this.btnFinance.setEnabled(true);
                }
                else{
                    this.btnFinance.setEnabled(false);
                }
            }
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }
    
